package main

import "fmt"

func main() {
	// text - Hello World
	var message string = "Go is amazing!"
	fmt.Println(message)

	// whole numbers - 14
	var number int = 14
	fmt.Println(number)

	// real numbers - 1.43
	var realNum float32 = 1.43
	fmt.Println(realNum)

	// characters - S, !
	var ch rune = 'S'
	fmt.Println(ch)

	// boolean - true or false
	var isTrue bool = true
	fmt.Println(isTrue)
}
